let cart = [];

function showCategory(category) {
    const productsSection = document.getElementById('products');
    productsSection.innerHTML = '';
    products[category].forEach(product => {
        const productItem = document.createElement('div');
        productItem.className = 'product-item';
        productItem.innerHTML = `
            <img src="${product.img}" alt="${product.name}">
            <h3>${product.name}</h3>
            <p class="price">$${product.price}</p>
            <button onclick="addToCart('${product.name}', ${product.price})">Agregar</button>
            <a href="#" class="plans">Ver planes de cuotas</a>
            <input type="checkbox" class="compare"> Comparar
        `;
        productsSection.appendChild(productItem);
    });
}

function addToCart(name, price) {
    const existingProduct = cart.find(item => item.name === name);
    if (existingProduct) {
        existingProduct.quantity++;
    } else {
        cart.push({ name, price, quantity: 1 });
    }
    updateCart();
}

function updateCart() {
    const cartItems = document.getElementById('cart-items');
    cartItems.innerHTML = '';
    let totalPrice = 0;
    cart.forEach(product => {
        totalPrice += product.price * product.quantity;
        const cartItem = document.createElement('li');
        cartItem.className = 'cart-item';
        cartItem.innerHTML = `
            ${product.name} - $${product.price} x ${product.quantity}
            <button onclick="removeFromCart('${product.name}')">-</button>
            <button onclick="addToCart('${product.name}', ${product.price})">+</button>
        `;
        cartItems.appendChild(cartItem);
    });
    document.getElementById('total-price').innerText = `$${totalPrice}`;
}

function removeFromCart(name) {
    const product = cart.find(item => item.name === name);
    if (product.quantity > 1) {
        product.quantity--;
    } else {
        cart = cart.filter(item => item.name !== name);
    }
    updateCart();
}

